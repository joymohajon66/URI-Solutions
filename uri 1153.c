#include<stdio.h>

int main()
{
    int i, n, s=1;

    scanf("%d",&n);

    // calculating factorial of n
    for(i=n; i>=1; i--)
    {

        s=s*i;
    }

    // displaying factorial result
    printf("%d\n",s);

    return 0;
}


